    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","varshaK@05");
            
            
            String rollno = rollnoTextField.getText();
            
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"'and result.rollno='"+rollno+"'");
            
            if(rs.next()) {
                courseTextField.setText(rs.getString(2));
                branchTextField.setText(rs.getString(3));
                nameTextField.setText(rs.getString(4));
                genderTextField.setText(rs.getString(5));
                fathernameTextField.setText(rs.getString(6));
                
                mathTextField.setText(rs.getString(8));
                mcTextField.setText(rs.getString(9));
                mpTextField.setText(rs.getString(10));
                comparchTextField.setText(rs.getString(11));
                sigsysTextField.setText(rs.getString(12));
                
                int s1 = Integer.parseInt(mathTextField.getText());
                int s2 = Integer.parseInt(mcTextField.getText());
                int s3 = Integer.parseInt(mpTextField.getText());
                int s4 = Integer.parseInt(comparchTextField.getText());
                int s5 = Integer.parseInt(sigsysTextField.getText());
                
                int total= s1+s2+s3+s4+s5;
                totalTextField.setText(String.valueOf(total));
                
                if(s1<33 || s2<33 || s3<33 || s4<33 || s5<33){
                    resultTextField.setText("FAIL");
                }
                else{
                    resultTextField.setText("PASS");
                }
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Student is not registered!");
            }
            
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

